#include<iostream>
using namespace std;
int main()
{
    int a=10,b=20,i=0;
    cout<<"sum of two numbers";
    while(b>0)
    {
        a++;
        b--;
    }
    cout<<a<<endl;
    int x=10,y=20;
    cout<<"substraction of two numbers";
    while(y>x)
    {
        i++;
        y--;
    }
    cout<<i;
    return 0; 
}